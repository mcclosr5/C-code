public class TenToOne
{
    public static void main(String [] args)
    {
        // Use a loop to print out the numbers
        // We will use i as the loop variable
        int i = 10; // Starting value
        for(i = 10; i > 0; i--) // Keep looping as long as some condition is true
        {
            System.out.print(i + " ");
        }
       
        System.out.println(); // Finish with a new line.
    }
}